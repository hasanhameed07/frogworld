
function getRandom(min, max) {
    return Math.floor(Math.random() * (max - min) + min);
}

function getXWithinBounds(x, width) {
    if (x-(width/2)<=0) 
    	return width/2;
    else if (x+(width/2)>=Pond.GAME_WIDTH)
    	return x-width;
    else
    	return x;
}

function getYWithinBounds(y, height) {
    if (y-(height/2)<=0) 
    	return height/2;
    else if (y+(height/2)>=Pond.GAME_HEIGHT)
    	return y-height;
    else
    	return y;
}

